package com.hzdp.controller;

import com.hzdp.web.BaseResponse;

public class FriendResponse extends BaseResponse {

	private int id;
	private String name;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
